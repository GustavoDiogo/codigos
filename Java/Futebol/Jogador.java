
public class Jogador {
	public String nome;
	public String time;
	public String posicao;	

	public Jogador(String nome, String posicao) {
		this.nome = nome;
		this.posicao = posicao;
	}
	
	public String getTime(){
		return this.time;
	}
	
	public String getPosicao(){
		return this.posicao;
	}
	
	public String getNome(){
		return this.nome;
	}
	
	
	
	
	

}
